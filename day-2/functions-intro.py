# Functions

# A Function is a block reusable code that performs a specific task!

# Why functions are good:
# - It makes code cleaner
# - Stops you repeatedly writing the same code (DRY)
# - Makes your code more modular --> Reusability, Maintainability, less goes wrong etc.

# Syntax
# def function_name():
    # return something

# Simple print
print("Hello")

def hello(name):
    print("hello " + name)

# Calling the function
hello("luke")
hello("Anita")

# Addition function example
def add(num1, num2):
    return num1 + num2

print(add(5, 5))
print(add(10, 10))
